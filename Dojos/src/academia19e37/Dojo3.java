// DRY: Don't Repeat Yourself. No Repitas Código
// ¿Eres capaz de no repetirte?

package academia19e37;

import java.util.Scanner;

public class Dojo3 {
	  
	public static void main(String[] args) {
		
      	Scanner sc = new Scanner(System.in);
      	int n;
      
      	System.out.println("Sabes que no debes repetir código!");
      	System.out.print("Dime un número: ");
      	n = sc.nextInt();
      	sc.close();
      	System.out.println("Pues escribe " + n + " veces: \"NO DEBO REPETIR CÓDIGO (DRY)\"");
      	//-----------------------------------------------------------------------------------
      	
      	
      
        //-----------------------------------------------------------------------------------
	}

}
